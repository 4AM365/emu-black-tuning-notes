def main() -> None:
    for name, original, anchors, out_name in [
        ("VE1", VE1, VE1_ANCHORS, "VE1_smoothed.csv"),
        ("VE2", VE2, VE2_ANCHORS, "VE2_smoothed.csv"),
    ]:
        print(f"\n{'='*60}")
        print(f"Smoothing {name} (degree-3 polynomial, 3 passes) ...")
        smoothed = smooth_table(original, anchors, degree=3, n_passes=3)

        nrow, ncol = original.shape
        mask = np.zeros((nrow, ncol), dtype=bool)
        for ri, ci in anchors:
            mask[ri, ci] = True

        print_table(f"{name} SMOOTHED", smoothed, RPM, LOAD, anchor_mask=mask)
        print_delta(original, smoothed, RPM, LOAD)

        out = ROOT / "supra" / "tunes" / out_name
        write_csv(smoothed, RPM, LOAD, out)


if __name__ == "__main__":
    main()
